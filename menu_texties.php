      <td>
        <p><span class="maindata">I wrote a whole bunch of text adventures during 
          the 80's and early 90's for the Amstrad CPC and PC which were converted 
          to the Spectrum and C64 by Tony Collins (Guild), Anthony Reynolds (Adventure 
          Workshop) and Dorothy Millard. Many sold commercially, and many were 
          released as public domain. (Known today as Freeware) Good times, a good 
          bunch of folks involved in the UK textie scene at that time.</span></p>
        <p><span class="maindata">I hereby release all my text adventures up until 
          2004 as public domain. All copyright notices are void insofar as they 
          apply to me. </span></p>
        <p><span class="maindata">If you have any versions of these games that 
          aren't listed below, I'd love a copy.</span></p>
        <p><span class="maindata">Thanks to Dorothy Millard and Frederic Herlem 
          for sending me copies of some of the following. Thanks also to Debby 
          Howard, D. Jones and Philip Reynolds for some of the solutions.</span></p>
        <p><span class="maindata">Here follows the games, and where available, 
          the snapshots, executable programs (PC) and perhaps even the source 
          code.</span></p>
        <p>Enjoy.</p>
